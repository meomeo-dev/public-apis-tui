function runCliHelp(args: string[]): string {
  return execFileSync(process.execPath, ['--import', 'tsx', 'src/cli.ts', ...args], {
    encoding: 'utf8',
    env: {
      ...process.env,
      NODE_NO_WARNINGS: '1',
    },
  })
}

function readOperationHelp(help: string, operationId: string): string {
  const marker = `Usage: ${operationId} [options]`
  const index = help.indexOf(marker)
  assert.notEqual(index, -1)
  return help.slice(index)
}
