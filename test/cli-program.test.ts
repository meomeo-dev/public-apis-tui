test('generic public API run rejects unsupported no-arg operation options with operation id', () => {
  assert.throws(
    () => runCliHelp(['apis', 'run', 'ukcarbonintensity.intensity', '--format', 'json', '--', '--limit', '1']),
    (error: unknown) => {
      const stderr = error !== null && typeof error === 'object' && 'stderr' in error
        ? String((error as { stderr: unknown }).stderr)
        : ''
      const message = error instanceof Error ? error.message : ''
      return /Unsupported option for ukcarbonintensity\.intensity: --limit/.test(`${stderr}\n${message}`)
    },
  )
})

