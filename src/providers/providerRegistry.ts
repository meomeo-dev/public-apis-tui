export function createPublicApiRegistry(providers: PublicApiProviderModule[]): PublicApiRegistry {
  validateUniqueProviderIds(providers)
  validateUniqueOperationIds(providers.flatMap(provider => provider.operations))
  validateOperationCliOptions(providers.flatMap(provider => provider.operations))
  return {
    providers: [...providers],
    manifests: providers.map(provider => provider.manifest),
    operations: providers.flatMap(provider => provider.operations),
  }
}

export function listPublicApiOperations(registry: PublicApiRegistry): PublicApiOperationDefinition[] {
  return [...registry.operations]
}

export function listPublicApiEndpoints(registry: PublicApiRegistry) {
  return registry.providers.flatMap(provider => provider.endpoints)
}

export function getPublicApiOperation(registry: PublicApiRegistry, operationId: string): PublicApiOperationDefinition {
  const operation = registry.operations.find(entry => entry.id === operationId || entry.rpcMethod === operationId)
  if (operation === undefined) {
    throw new RuntimeFailure('INVALID_ARGUMENT', `Unknown public API operation: ${operationId}`, {
      operationId,
      supported: registry.operations.map(entry => entry.id),
    })
  }

  return operation
}

export function findPublicApiOperationByCommandPath(
  registry: PublicApiRegistry,
  commandPath: string[],
): PublicApiOperationDefinition | undefined {
  return registry.operations.find(operation => operation.commandPath.join(' ') === commandPath.join(' '))
}

function validateUniqueProviderIds(providers: PublicApiProviderModule[]): void {
  const ids = new Set<string>()
  for (const provider of providers) {
    if (ids.has(provider.manifest.id)) {
      throw new RuntimeFailure('INVALID_ARGUMENT', `Duplicate provider id: ${provider.manifest.id}`)
    }
    ids.add(provider.manifest.id)
  }
}

function validateUniqueOperationIds(operations: PublicApiOperationDefinition[]): void {
  const ids = new Set<string>()
  const rpcMethods = new Set<string>()
  for (const operation of operations) {
    if (ids.has(operation.id)) {
      throw new RuntimeFailure('INVALID_ARGUMENT', `Duplicate operation id: ${operation.id}`)
    }
    if (rpcMethods.has(operation.rpcMethod)) {
      throw new RuntimeFailure('INVALID_ARGUMENT', `Duplicate RPC method: ${operation.rpcMethod}`)
    }
    ids.add(operation.id)
    rpcMethods.add(operation.rpcMethod)
  }
}

function validateOperationCliOptions(operations: PublicApiOperationDefinition[]): void {
  for (const operation of operations) {
    const optionNames = new Set<string>()
    const optionFlags = new Set<string>()
    for (const option of operation.options) {
      validateOperationCliOption(operation.id, option, optionNames, optionFlags)
    }
  }
}

function validateOperationCliOption(
  operationId: string,
  option: PublicApiOperationOption,
  optionNames: Set<string>,
  optionFlags: Set<string>,
): void {
  if (optionNames.has(option.name)) {
    throw new RuntimeFailure('INVALID_ARGUMENT', `Duplicate CLI option name for ${operationId}: ${option.name}`)
  }
  optionNames.add(option.name)

  if (optionFlags.has(option.flag)) {
    throw new RuntimeFailure('INVALID_ARGUMENT', `Duplicate CLI option flag for ${operationId}: ${option.flag}`)
  }
  optionFlags.add(option.flag)

  if (option.reason.trim() === '') {
    throw new RuntimeFailure('INVALID_ARGUMENT', `CLI option ${operationId}.${option.name} must include a UX exposure reason.`)
  }

  if (!['primary', 'advanced', 'hidden'].includes(option.exposure)) {
    throw new RuntimeFailure('INVALID_ARGUMENT', `CLI option ${operationId}.${option.name} has unsupported UX exposure: ${option.exposure}`)
  }

  if (publicApiOperationOptionGroupLabels[option.group] === undefined) {
    throw new RuntimeFailure('INVALID_ARGUMENT', `CLI option ${operationId}.${option.name} has unsupported UX group: ${option.group}`)
  }
}
